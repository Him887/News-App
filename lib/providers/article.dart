class Article {
  String name;
  String author;
  String title;
  String description;
  String imageUrl;
  String date;
  String content;
  String url;
  Article({
    required this.name,
    required this.author,
    required this.title,
    required this.description,
    required this.imageUrl,
    required this.date,
    required this.content,
    required this.url
  });
}
